clc;
clear;
close all;

%% =========================
% 1. 基本参数设置
%% =========================
fs = 2000;              % 采样频率 (Hz)
t = 0:1/fs:0.2;         % 时间轴 (s)

%% =========================
% 2. 生成更复杂的信号
%   含有多种频率信号：50 Hz, 100 Hz, 200 Hz, 500 Hz
%   添加高频噪声
%% =========================
f1 = 50;                % 有用信号频率1
f2 = 100;               % 有用信号频率2
f3 = 200;               % 有用信号频率3
f4 = 500;               % 高频干扰信号

% 生成正弦波信号
x1 = sin(2*pi*f1*t);
x2 = 0.5*sin(2*pi*f2*t);
x3 = 0.3*sin(2*pi*f3*t);
x4 = 0.2*sin(2*pi*f4*t);

% 添加噪声（白噪声）
noise = 0.3 * randn(size(t));  % 噪声

% 合成信号
x = x1 + x2 + x3 + x4 + noise;

%% =========================
% 3. 设计低通滤波器
%   截止频率 100 Hz
%% =========================
fc = 100;               % 截止频率 (Hz)
order = 4;              % 滤波器阶数

Wn = fc / (fs/2);       % 归一化截止频率
[b, a] = butter(order, Wn, 'low');

%% =========================
% 4. 滤波处理
%% =========================
y = filter(b, a, x);

%% =========================
% 5. 时域波形对比
%% =========================
figure;
subplot(2,1,1);
plot(t, x);
xlabel('时间 / s');
ylabel('幅值');
title('滤波前信号时域波形');

subplot(2,1,2);
plot(t, y);
xlabel('时间 / s');
ylabel('幅值');
title('滤波后信号时域波形');

%% =========================
% 6. 频域分析（FFT）
%% =========================
N = length(x);
f = (0:N-1) * fs / N;

X = abs(fft(x));
Y = abs(fft(y));

figure;
subplot(2,1,1);
plot(f, X);
xlim([0 500]);
xlabel('频率 / Hz');
ylabel('幅度');
title('滤波前信号频谱');

subplot(2,1,2);
plot(f, Y);
xlim([0 500]);
xlabel('频率 / Hz');
ylabel('幅度');
title('滤波后信号频谱');
